// Include database connection
require_once '../includes/db.php';

$message = '';
$message_type = '';
$errors = [];

// Handle form submission
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    // Validate and sanitize input
    $title = trim($_POST['title'] ?? '');
    $author = trim($_POST['author'] ?? '');
    $category = trim($_POST['category'] ?? '');
    $published_year = trim($_POST['published_year'] ?? '');
    $published_month = trim($_POST['published_month'] ?? '');
    $status = $_POST['status'] ?? 'Available';
    
    // Validation
    if (empty($title)) {
        $errors[] = "Book title is required.";
    }
    
    if (empty($author)) {
        $errors[] = "Author name is required.";
    }
    
    if (empty($category)) {
        $errors[] = "Category is required.";
    }
    
    if (!empty($published_year) && (!is_numeric($published_year) || $published_year < 1000 || $published_year > date('Y'))) {
        $errors[] = "Please enter a valid publication year.";
    }
    
    // Handle file upload
    $book_image = null;
    if (isset($_FILES['book_image']) && $_FILES['book_image']['error'] === UPLOAD_ERR_OK) {
        $upload_dir = '../uploads/book_images/';
        
        // Create directory if it doesn't exist
        if (!file_exists($upload_dir)) {
            mkdir($upload_dir, 0777, true);
        }
        
        $file_tmp = $_FILES['book_image']['tmp_name'];
        $file_name = $_FILES['book_image']['name'];
        $file_size = $_FILES['book_image']['size'];
        $file_ext = strtolower(pathinfo($file_name, PATHINFO_EXTENSION));
        
        // Validate file
        $allowed_extensions = ['jpg', 'jpeg', 'png', 'gif', 'webp'];
        $max_file_size = 5 * 1024 * 1024; // 5MB
        
        if (!in_array($file_ext, $allowed_extensions)) {
            $errors[] = "Only JPG, JPEG, PNG, GIF, and WEBP files are allowed.";
        } elseif ($file_size > $max_file_size) {
            $errors[] = "File size must be less than 5MB.";
        } else {
            // Generate unique filename
            $book_image = uniqid() . '_' . time() . '.' . $file_ext;
            $upload_path = $upload_dir . $book_image;
            
            if (!move_uploaded_file($file_tmp, $upload_path)) {
                $errors[] = "Failed to upload image.";
                $book_image = null;
            }
        }
    }
    
    // If no errors, insert into database (book_code will be auto-generated by trigger)
    if (empty($errors)) {
        try {
            // Insert into database - the trigger will automatically generate book_code
            $sql = "INSERT INTO books (title, author, category, published_year, published_month, book_image, status, created_at) 
                    VALUES (?, ?, ?, ?, ?, ?, ?, NOW())";
            
            $stmt = $conn->prepare($sql);
            $result = $stmt->execute([
                $title,
                $author,
                $category,
                $published_year ?: null,
                $published_month ?: null,
                $book_image,
                $status
            ]);
            
            if ($result) {
                // Get the generated book_code
                $book_id = $conn->lastInsertId();
                $codeStmt = $conn->prepare("SELECT book_code FROM books WHERE book_id = ?");
                $codeStmt->execute([$book_id]);
                $generated_code = $codeStmt->fetchColumn();
                
                $message = "Book added successfully with ID: <strong>{$generated_code}</strong>";
                $message_type = "success";
                
                // Clear form data on success
                $title = $author = $category = $published_year = $published_month = '';
                $status = 'Available';
            } else {
                $errors[] = "Failed to add book to database.";
            }
            
        } catch (PDOException $e) {
            if ($e->getCode() == 23000) { // Duplicate entry
                $errors[] = "A book with this combination already exists. Please try again.";
            } else {
                $errors[] = "Database error: " . $e->getMessage();
            }
        }
    }
    
    if (!empty($errors)) {
        $message_type = "error";
    }
}

